@app.cell
def _(mo, output_path, prediction_done):
    import zipfile as _zipfile
    from io import BytesIO as _BytesIO
    from pathlib import Path as _Path

    import pandas as _pd

    mo.stop(not prediction_done)

    _predicted_path = output_path / "data" / "results_predicted.xlsx"
    _data_dir = output_path / "data"

    _elements = []

    # Zip output folder
    _zip_buf = _BytesIO()
    with _zipfile.ZipFile(_zip_buf, "w", _zipfile.ZIP_DEFLATED) as _zf:
        for _f in sorted(_Path(output_path).rglob("*")):
            if _f.is_file():
                _zf.write(_f, _f.relative_to(output_path))
    _zip_buf.seek(0)
    _elements.append(mo.download(data=_zip_buf.getvalue(), filename="spx_unmixing_results.zip", label="Download all results (.zip)"))

    # Results table
    if _predicted_path.exists():
        _df = _pd.read_excel(_predicted_path)
        _elements.append(mo.md("### Prediction Results"))
        _elements.append(mo.ui.table(_df))

    # Files table with individual downloads
    _all_files = []
    if _data_dir.exists():
        for _f in sorted(_data_dir.iterdir()):
            if _f.is_file():
                _all_files.append({
                    "file": _f.name,
                    "size": f"{_f.stat().st_size / 1024:.1f} KB",
                    "download": mo.download(data=_f.read_bytes(), filename=_f.name, label="Download"),
                })

    if _all_files:
        _elements.append(mo.md("### All Output Files"))
        _elements.append(mo.ui.table(_all_files, selection=None))

    mo.vstack(_elements)
    return


