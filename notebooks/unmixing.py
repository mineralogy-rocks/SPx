@app.cell
def _(endmembers_path, mo, run_button, run_prediction, settings):
    import io as _io
    import logging as _logging

    mo.stop(not run_button.value, mo.md("Click **Run Spectral Unmixing** above to start."))
    mo.stop(endmembers_path is None, mo.md("Please upload an **endmembers.xlsx** file first."))

    _log_buf = _io.StringIO()
    _handler = _logging.StreamHandler(_log_buf)
    _handler.setLevel(_logging.INFO)
    _handler.setFormatter(_logging.Formatter("%(asctime)s %(levelname)s %(message)s"))

    _root = _logging.getLogger()
    _root.addHandler(_handler)

    try:
        with mo.status.spinner("Running spectral unmixing..."):
            run_prediction(endmembers_path=endmembers_path)
    finally:
        _root.removeHandler(_handler)

    prediction_done = True
    output_path = settings.OUTPUT_PATH
    prediction_log = _log_buf.getvalue()
    return output_path, prediction_done, prediction_log


